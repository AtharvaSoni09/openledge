const stateByCode = new Map(US_STATES.map((state) => [state.code, state.name]));
const stateByName = new Map(
  US_STATES.map((state) => [state.name.toLowerCase(), state.code]),
);

export function normalizeStateCode(input: string): string | null {
  const cleaned = input.trim().toUpperCase();
  if (cleaned === 'US' || cleaned === 'ALL' || cleaned === 'NATIONAL' || cleaned === 'NATIONALLY') return 'US';
  if (stateByCode.has(cleaned)) return cleaned;
  const fromName = stateByName.get(input.trim().toLowerCase());
  return fromName ?? null;
}

export function stateNameFromCode(code: string): string {
  if (code.toUpperCase() === 'US') return 'All / Nationally';
  return stateByCode.get(code.toUpperCase()) ?? code;
}
